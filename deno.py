def deno(a):
    s2000,s500,s200,s100,s50,s20,s1=0,0,0,0,0,0,0
    while a>1:
        if a>2000:
            s2000=a//2000
            a=a-(2000*s2000)
        elif a>500:
            s500=a//500
            a=a-(500*s500)
        elif a>200:
            s200=a//200
            a=a-(200*s200)
        elif a>100:
            s100=a//100
            a=a-(100*s100)
        elif a>50:
            s50=a//50
            a=a-(50*s50)
        elif a>20:
            s20=a//20
            a=a-(20*s20)
        elif a>1:
            s1=a//1
            a=a-(1*s1)
    s=f"2000 : {s2000} \\ 500 : {s500} \\ 200 : {s200} \\ 100 : {s100} \\ 50 : {s50} \\ 20 : {s20} \\ 1 : {s1} \\"
    return s
